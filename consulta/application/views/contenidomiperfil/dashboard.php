<script type="text/javascript">

    var timermiau = 0;
    $(document).on('touchend', '.fc-day-grid-container td', function() {
        if (timermiau == 0) {
            // set first click

            timermiau = new Date().getTime();
        } else {
            // compare first click to this click and see if they occurred within double click threshold
            if (((new Date().getTime()) - timermiau) < 800) {
                // double click occurred

                var fechaformatonormal = $(this).data('fecha');
                var fechaformatosql = $(this).data('date');

                var date = new Date();
                var datecompare = new Date(fechaformatosql);

                $('#info').modal('show');
                $('#fecha').val(fechaformatonormal);
                $('#date').val(fechaformatosql);
                $('.classidinputs').removeAttr("disabled");

                if (datecompare < date) {

                    $('.classidinputs').attr("disabled", "");

                }
                timermiau = 0;
            } else {
                // not a double click so set as a new first click
                timermiau = new Date().getTime();
            }
        }
    });
</script>
